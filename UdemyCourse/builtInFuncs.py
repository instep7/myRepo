# returns absolute value
print(abs(-4))

# returns True/False result
print(bool(1 > 2))

# returns possible functions that can be used on the parameter
print(dir("Hello"))

# returns explanation of function (type q to exit the window that pops up)
print(help("Hello".upper))

# evaluates the string as a line of code
sent = "print('HI')"
eval(sent)

# type conversion of str, int, and float
print("hello " + str(100))
print(123 + int("456"))
print(float("123.45") + 0.23)